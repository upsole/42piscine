int	ft_sqrt(int nb)
{
	int	x;
	int	y;

	x = nb;
	y = 1;
	while ((x - y) > 1)
	{
		y = nb / x;
		x = (x + y) / 2;
	}
	if ((x * x) == nb)
		return (x);
	return (0);
}
